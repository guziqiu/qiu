#include<stdio.h>
int main(int argc,const char* argv[]){
	int n;
	n = 1;
	while (n <= 100) {
		printf("%d\n", n);
		n++;
	}
	for (int i = 1; i <= 100; i++){
	printf("%d\n", i);
	}
	return 0;
}
